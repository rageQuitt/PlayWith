<?php

namespace App\Repository;

use App\Entity\Products;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Products>
 *
 * @method Products|null find($id, $lockMode = null, $lockVersion = null)
 * @method Products|null findOneBy(array $criteria, array $orderBy = null)
 * @method Products[]    findAll()
 * @method Products[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class ProductsRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Products::class);
    }

    public function save(Products $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Products $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }
    public function findFirstProductInCategories($categories)
    {
        return $this->getEntityManager()->createQuery('
            SELECT p
            FROM App\Entity\Products p
            WHERE p.categories = :categories
            ORDER BY p.id ASC
        ')
        ->setParameter('categories', $categories)
        ->setMaxResults(1)
        ->getOneOrNullResult();
    }
    public function getCartDetails(Request $request, ProductsRepository $productsRepository) {
        $cart = $request->getSession()->get('cart', []);
        $cartDetails = [];
    
        foreach ($cart as $productId => $quantity) {
            $product = $productsRepository->find($productId);
            $cartDetails[] = [
                'product' => $product,
                'quantity' => $quantity
            ];
        }
    
        return $cartDetails;
    }
    

